def main():
    csv_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "config", "items_master.csv")

    with open(csv_path, "r", encoding="utf-8-sig") as f:
        content = f.read()

    reader = csv.DictReader(io.StringIO(content))
    fieldnames = reader.fieldnames
    rows = list(reader)

    names_used = set()
    for row in rows:
        item_id = row["item_id"]
        if item_id in ITEMS_DATA:
            new_name, new_flavor = ITEMS_DATA[item_id]
            if new_name in names_used:
                print(f"WARNING: Duplicate name '{new_name}' for {item_id}")
            names_used.add(new_name)
            row["item_name"] = new_name
            row["flavor_text"] = new_flavor

    # Write back with UTF-8 BOM + CRLF
    output = io.StringIO()
    writer = csv.DictWriter(output, fieldnames=fieldnames, lineterminator="\r\n")
    writer.writeheader()
    writer.writerows(rows)

    out_path = os.path.join(os.path.dirname(csv_path), "items_master_new.csv")
    with open(out_path, "wb") as f:
        f.write(b"\xef\xbb\xbf")  # UTF-8 BOM
        f.write(output.getvalue().encode("utf-8"))

    print(f"Done. Updated {len(names_used)} items. Total unique names: {len(names_used)}")
    if len(names_used) != 258:
        missing = set(f"itm_{i:05d}" for i in range(1, 259)) - set(ITEMS_DATA.keys())
        if missing:
            print(f"Missing item IDs: {sorted(missing)}")


if __name__ == "__main__":
    main()
